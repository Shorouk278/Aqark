    func addFreeAdvertisement(){
        dataBaseRef.child("Users_Ads").child(userID).child("countOfFree").observeSingleEvent(of: .value) { (data) in
            if let value = data.value{
                if value is NSNull{
                    self.addFirstFreeAdvertisement()
                }else{
                    let numberOFAds = value as! Int
                    self.addSecondFreeAdvertisement(numberOFAds : numberOFAds)
                }
            }
        }
    }
    
    
    
    
